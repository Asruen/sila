<p>Dalam hal ini bertindak untuk dan atas nama ……………………. Kab. Sukoharjo yang selanjutnya disebut sebagai <b>Pihak Kedua</b>.</p>

<p>Kedua belah pihak sepakat untuk mengikatkan diri dalam perjanjian Kerjasama dengan ketentuan sebagai berikut:</p>

<ol>
    <li>Pihak pertama akan mengirimkan paket makan bergizi gratis kepada pihak kedua terhitung mulai 9 Desember 2024 sampai 20 Desember 2024 sebagai tahap peninjauan dan berlaku selama 5 tahun dengan perpanjangan setiap 1 tahun.</li>
    <li>Pihak kedua akan menerima paket makan bergizi gratis dan membagikan kepada seluruh 1 siswa.</li>
    <li>Jumlah paket makan bergizi disesuaikan dengan data yang telah diberikan oleh pihak kedua.</li>
    <li>Pihak kedua diwajibkan mengembalikan alat dan tempat makan setelah siswa selesai makan sesuai dengan jumlah paket makan bergizi yang dikirimkan.</li>
    <li>Apabila terdapat kerusakan dan atau kehilangan alat makan (tutup, dan tray tempat makan) pihak kedua diwajibkan untuk mengganti atau membayar seharga satu paket tempat makan (Rp. 60.000,00/pcs) sesuai dengan jumlah kerusakan atau kehilangan.</li>
    <li>Apabila terjadi bencana, pengembalian alat dan tempat makan dilakukan setelah situasi stabil dengan inventarisasi terlebih dahulu oleh Pihak Kedua.</li>
    <li>Apabila terjadi Kejadian Luar Biasa, seperti keracunan, ketidaklengkapan paket makanan, atau kondisi lain yang dapat mengganggu kelancaran pelaksanaan program ini, Pihak Kedua berkomitmen untuk menjaga kerahasiaan informasi hingga Pihak Pertama menemukan solusi terbaik untuk menyelesaikan masalah tersebut. Kedua belah pihak sepakat untuk saling berkomunikasi dan bekerja sama dalam mencari solusi terbaik demi kelangsungan program ini.</li>
    <li>Pihak Kedua memberitahukan kepada siswa untuk peralatan makan (sendok dan garpu) wajib membawa secara pribadi.</li>
    <li>Pihak Kedua mengutus 1 koordinator sebagai penanggungjawab kelancaran pendistribusian makan bergizi gratis.</li>
</ol>

<p>Demikian surat perjanjian Kerjasama ini dibuat dengan penuh kesadaran dan tanggung jawab. Selanjutnya surat perjanjian ini akan bersifat mengikat selama masa berlakunya.</p>

<p>Jika terjadi perselisihan selama masa perjanjian, akan diselesaikan secara mufakat. Jalur hukum akan ditempuh apabila kedua belah pihak tidak menemui titik mufakat.</p>

